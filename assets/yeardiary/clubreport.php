            <h3>Club Report</h3>
            Month of November,2014.
            “Imagination is the beginning of creation. You imagine what you desire, you will what you imagine and at last you create what you will”
            This being the inaugural month of the club website it is amazing to see that the passion with which we began an endeavour has finally materialized. The site in existence is the evidence in full resolve that what we wish for shall be realized only with true intent. This month saw the conduction of yet another immensely successful BDC with the donations crossing the record breaking 175+ units.  Held on the 9th of this month, with the able collaboration of the Jalan Blood Bank ,the blood donation camp was attended in full vigour by the students and the faculty members apart. The event was inaugurated by the director of the institute, Dr.(Prof).U.K.Dey with the club in charge, Prof.Amar Kumar and others to mark the occasion. Director Sir commended the efforts of the club members in carrying out a humane task and remarked that this annual event organized by the Leo Club of BIT Sindri has always been one of the most genuine events in the campus.
